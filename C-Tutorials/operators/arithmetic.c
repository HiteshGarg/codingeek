#include <stdio.h>
#include <stdlib.h>

int main()
{
    int a = 16, b = 5, c = 0;
    
    c=a+b;
    printf("the sum is: %d\n", c);
    
    c=a-b;
    printf("the difference is: %d\n", c);
    
    c=a*b;
    printf("the product is: %d\n", c);
    
    c=a/b;
    printf("the quotient is: %d\n", c);
    
    c=a%b;
    printf("the remainder is: %d\n", c);
    
    return 0;
}
